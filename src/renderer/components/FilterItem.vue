<script>

import EventBus from '../utils/bus'
import moment from 'moment'

export default {
    methods:{
        getToday(){
            return (new Date()).getDate()
        },
        onFilterClick(){
            if(this.filterId !== this.data.id){
                this.$store.commit('updateFilterId',this.data.id)
            }
        },
        onContextMenu(event){
            if(this.data.id < 0){
                return
            }
            EventBus.$emit('showContext',{data:this.data,clickX:event.clientX,clickY:event.clientY})
        }
    },

    computed:{
        className(){
            if(this.filterId === this.data.id){
                return 'slidebar-item active'
            }
            return 'slidebar-item'
        },
        overdayNum(){
            return 0
        },
        todoNum(){
            if(this.data.id >= 0 || this.data.id === -4){
                let list = this.todoList.filter(item=>item.filterId === this.data.id)
                return list.length
            }
            if(this.data.id === -3){
                let list = this.todoList.filter(item=>item.star === 1)
                return list.length
            }
            if(this.data.id === -2){
                let list = this.todoList.filter(item=>{
                    let cData = moment(item.date).format('WW,YYYY')
                    let now = moment().format('WW,YYYY')
                    console.log(cData,now)
                    return cData === now
                })
                return list.length
            }
            if(this.data.id === -1){
                let list = this.todoList.filter(item=>{
                    let cData = moment(item.date).format('DD,MM,YYYY')
                    let now = moment().format('DD,MM,YYYY')
                    return cData === now
                })
                return list.length
            }
        },
        todoItemByFilter(){

            return this.todoList.filter((item)=>item.filterId === this.data.id)
        }
    },
    props:{
        data:{
            type:Object,
            required:true
        },
        filterId:{
            type:[String,Number]
        },
        todoList:{
            type:Array
        }
    }
}
</script>
